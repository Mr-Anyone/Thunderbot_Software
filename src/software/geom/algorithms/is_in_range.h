#pragma once

/**
 * Returns whether a value is within a range. Note that the range can be specified in
 * either order.
 *
 * @param val the value to check
 * @param range1 the first value of the range
 * @param range2 the second value of the range
 * @return whether the value is within the range
 */
bool isInRange(double val, double range1, double range2);
